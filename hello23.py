a=float(input("Enter The Marks Obtained"))
if a<=100 and a>=90:
    print("The Grade is A1")
elif a<=90 and a>=80:
    print("The Grade is A2")
elif a<=80 and a>=70:
    print("The Grade is B1")
elif a<=70 and a>=60:
    print("The Grade is B2")
elif a<=60 and a>=50:
    print("The Grade is c1")
elif a<=50 and a>=40:
    print("The Grade is C2")
elif a<=40 and a>=30:
    print("The Grade is D1")
elif a<=30 and a>=20:
    print("The Grade is D2")
else:
    print("You have failed")
